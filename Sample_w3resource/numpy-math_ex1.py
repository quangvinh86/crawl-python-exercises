'''Question: 
Write a Python program to add, subtract, multiply, divide arguments element-wise.
Sample elements: 4.0, 1.2
'''

# Python code: 

import numpy as np
print("Add:")
print(np.add(1.0, 4.0))
print("Subtract:")
print(np.subtract(1.0, 4.0))
print("Multiply:")
print(np.multiply(1.0, 4.0))
print("Divide:")
print(np.divide(1.0, 4.0))


